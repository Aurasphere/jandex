	/**
	 * Creates a translation direction which specifies the original and
	 * destination language for a translation. The resulting string will be made
	 * up by the original language and the destination language divided by an
	 * hyphen.
	 * 
	 * @param from
	 *            the original language
	 * @param to
	 *            the destination language
	 * @return a translation direction string
	 * @see Jyandex#translateText(String, String)
	 */
